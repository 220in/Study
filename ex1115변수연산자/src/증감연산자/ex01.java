package 증감연산자;

public class ex01 {

	public static void main(String[] args) {
		
		// ++a와 a++ 차이(줄 단위로 생각)
		int a = 3;
		System.out.println(++a); // --> 4
		System.out.println(a); // --> 4
		// ++가 즉시 증가 후 출력
	
		int b = 3;
		System.out.println(b++); // --> 3
		System.out.println(b); // --> 4
		// ++가 출력 후 증가해서 출력 
		
		
		
		

	}

}
